package dda.silabo.clases.unidos;

/**
 *
 * @author Adry Qp
 */
public class DocenteUnidos {

    private String cedula;
    private String nombres;
    private String apellidos;
    private String telefono;
    private String correo;
    private String tituloTercer;
    private String tituloPost;
    private String selected;

    public DocenteUnidos() {
    }

    /**
     * @return the nombres
     */
    public String getNombres() {
        return nombres;
    }

    /**
     * @param nombres the nombres to set
     */
    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    /**
     * @return the apellidos
     */
    public String getApellidos() {
        return apellidos;
    }

    /**
     * @param apellidos the apellidos to set
     */
    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    /**
     * @return the telefono
     */
    public String getTelefono() {
        return telefono;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    /**
     * @return the cedula
     */
    public String getCedula() {
        return cedula;
    }

    /**
     * @param cedula the cedula to set
     */
    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    /**
     * @return the correo
     */
    public String getCorreo() {
        return correo;
    }

    /**
     * @param correo the correo to set
     */
    public void setCorreo(String correo) {
        this.correo = correo;
    }

    /**
     * @return the tituloTercer
     */
    public String getTituloTercer() {
        return tituloTercer;
    }

    /**
     * @param tituloTercer the tituloTercer to set
     */
    public void setTituloTercer(String tituloTercer) {
        this.tituloTercer = tituloTercer;
    }

    /**
     * @return the tituloPost
     */
    public String getTituloPost() {
        return tituloPost;
    }

    /**
     * @param tituloPost the tituloPost to set
     */
    public void setTituloPost(String tituloPost) {
        this.tituloPost = tituloPost;
    }

    /**
     * @return the selected
     */
    public String getSelected() {
        return selected;
    }

    /**
     * @param selected the selected to set
     */
    public void setSelected(String selected) {
        this.selected = selected;
    }

}

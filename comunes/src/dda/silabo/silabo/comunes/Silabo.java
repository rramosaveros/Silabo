package dda.silabo.silabo.comunes;

/**
 *
 * @author Jorge Zaruma
 */
public class Silabo {

    private Integer idSilabo;
    private Integer idUnidad;
    private String codMateria;
    private String codCarrera;
    private String cedula;
    private String tipo;
    private Integer idTipo;
    private String periodo;
    private String rol;
    private String tipoSeccion;
    private String campoFormacion;

    /**
     * @return the idSilabo
     */
    public Integer getIdSilabo() {
        return idSilabo;
    }

    /**
     * @param idSilabo the idSilabo to set
     */
    public void setIdSilabo(Integer idSilabo) {
        this.idSilabo = idSilabo;
    }

    /**
     * @return the idUnidad
     */
    public Integer getIdUnidad() {
        return idUnidad;
    }

    /**
     * @param idUnidad the idUnidad to set
     */
    public void setIdUnidad(Integer idUnidad) {
        this.idUnidad = idUnidad;
    }

    /**
     * @return the codMateria
     */
    public String getCodMateria() {
        return codMateria;
    }

    /**
     * @param codMateria the codMateria to set
     */
    public void setCodMateria(String codMateria) {
        this.codMateria = codMateria;
    }

    /**
     * @return the codCarrera
     */
    public String getCodCarrera() {
        return codCarrera;
    }

    /**
     * @param codCarrera the codCarrera to set
     */
    public void setCodCarrera(String codCarrera) {
        this.codCarrera = codCarrera;
    }

    /**
     * @return the cedula
     */
    public String getCedula() {
        return cedula;
    }

    /**
     * @param cedula the cedula to set
     */
    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the periodo
     */
    public String getPeriodo() {
        return periodo;
    }

    /**
     * @param periodo the periodo to set
     */
    public void setPeriodo(String periodo) {
        this.periodo = periodo;
    }

    /**
     * @return the rol
     */
    public String getRol() {
        return rol;
    }

    /**
     * @param rol the rol to set
     */
    public void setRol(String rol) {
        this.rol = rol;
    }

    /**
     * @return the campoFormacion
     */
    public String getCampoFormacion() {
        return campoFormacion;
    }

    /**
     * @param campoFormacion the campoFormacion to set
     */
    public void setCampoFormacion(String campoFormacion) {
        this.campoFormacion = campoFormacion;
    }

    /**
     * @return the idTipo
     */
    public Integer getIdTipo() {
        return idTipo;
    }

    /**
     * @param idTipo the idTipo to set
     */
    public void setIdTipo(Integer idTipo) {
        this.idTipo = idTipo;
    }

    /**
     * @return the tipoSeccion
     */
    public String getTipoSeccion() {
        return tipoSeccion;
    }

    /**
     * @param tipoSeccion the tipoSeccion to set
     */
    public void setTipoSeccion(String tipoSeccion) {
        this.tipoSeccion = tipoSeccion;
    }

}
